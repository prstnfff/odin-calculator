

function operate(operand, num1, num2){


    if(operand === 'add'){

        return(num1 + num2)

    } else if (operand === 'subtract'){

        return(num1 - num2)

    } else if (operand === 'multiply'){

        return(num1 * num2)
        
    } else if (operand === 'divide'){

        return(num1 / num2)
        
    }
}

